package microEmpresaUML;

public class Fundacao extends Ong {
	
	private String dataDeConstituicao;
	private String areaDeAtuacao;
	private boolean possuiFinsLucrativos;
	
	
	public String getDataDeConstituicao() {
		return dataDeConstituicao;
	}
	public void setDataDeConstituicao(String dataDeConstituicao) {
		this.dataDeConstituicao = dataDeConstituicao;
	}
	public String getAreaDeAtuacao() {
		return areaDeAtuacao;
	}
	public void setAreaDeAtuacao(String areaDeAtuacao) {
		this.areaDeAtuacao = areaDeAtuacao;
	}
	public boolean isPossuiFinsLucrativos() {
		return possuiFinsLucrativos;
	}
	public void setPossuiFinsLucrativos(boolean possuiFinsLucrativos) {
		this.possuiFinsLucrativos = possuiFinsLucrativos;
	}
	
	

}
